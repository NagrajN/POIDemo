-(SaveThePOIAppDelegate *) Global
{
    return (SaveThePOIAppDelegate *)[[UIApplication sharedApplication] delegate];
}


- (NSInteger)numberOfSectionsInTableView:(UITableView *)tableView
{
    // Return the number of sections.
    return 1;
}

- (NSInteger)tableView:(UITableView *)tableView numberOfRowsInSection:(NSInteger)section
{
    // Return the number of rows in the section.
    
    NSMutableArray *poiArray = [self.Global getPOIArray];
    
    int count = [poiArray count];
    
    return count;
}

- (UITableViewCell *)tableView:(UITableView *)tableView cellForRowAtIndexPath:(NSIndexPath *)indexPath
{
    
    UITableViewCell * cell = [tableView dequeueReusableCellWithIdentifier:@"POICell"];
    
    NSMutableArray *poiArray = [self.Global getPOIArray];
    
    int row = indexPath.row;
    
    POIData *poiData = (POIData *) [poiArray objectAtIndex:row];
    
    cell.textLabel.text = poiData.searchText;
    cell.detailTextLabel.text = [NSString stringWithFormat:@"%1$@ , %2$@", poiData.latitude,poiData.longitude];
    
    return cell;}

